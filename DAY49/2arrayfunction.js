let cars=["audi","bmw","toyota"];
//add elemnet to the last of array.
cars.push("maruti");
console.log(cars);
//pop function is used to delete last array element from the array.
console.log(cars.pop());
console.log(cars);
//unshift is used to add array at 0 index.
cars.unshift("maruti");
console.log(cars);
//shift deletes from start and return it.
cars.shift();
console.log(cars);